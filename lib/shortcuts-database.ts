// Helper functions for searching and filtering
export function getAllShortcuts(): Shortcut[] {
  return shortcutsDatabase.flatMap((app) =>
    app.shortcuts.map((shortcut) => ({
      ...shortcut,
      category: app.category,
      conflictLevel: app.conflictLevel,
    })),
  )
}

export function searchShortcuts(query: string): Shortcut[] {
  const allShortcuts = getAllShortcuts()
  const normalizedQuery = query.toLowerCase().replace(/\s+/g, "")

  return allShortcuts.filter((shortcut) => {
    const normalizedKeys = shortcut.keys.toLowerCase().replace(/\s+/g, "")
    const normalizedDescription = shortcut.description.toLowerCase()

    return normalizedKeys.includes(normalizedQuery) || normalizedDescription.includes(query.toLowerCase())
  })
}

export function findConflicts(inputKeys: string): Shortcut[] {
  const normalizedInput = inputKeys.toLowerCase().replace(/\s+/g, "")
  const allShortcuts = getAllShortcuts()

  return allShortcuts.filter((shortcut) => {
    const normalizedKeys = shortcut.keys.toLowerCase().replace(/\s+/g, "")
    return normalizedKeys === normalizedInput
  })
}

export function getShortcutsByApp(appName: string): AppShortcuts | undefined {
  return shortcutsDatabase.find((app) => app.name.toLowerCase() === appName.toLowerCase())
}

export function getShortcutsByCategory(category: ShortcutCategory): AppShortcuts[] {
  return shortcutsDatabase.filter((app) => app.category === category)
}
